# List in Python
students = ["John", "Jennie", "Jim", "Jack", "Joe"]
print(students, type(students))

# 1. Concatenation
print(students + ["Fionna", "George"])
print(students)

print()

# 2. Repetition
print(students * 2)
print(students)

print()

# 3. Membership Testing
print("John" in students)

# 4. Indexing
print(students[2])

# 5. Slicing
print(students[1:4])

